"""## Cell 5 — Find Best Batch Size

Runs a short timed trial over candidate batch sizes. Picks the largest size that doesn’t OOM **and** has the best prompts/sec. Sets `BATCH_SIZE` for the full run below.

Requires: model + tokenizer loaded, and `df_base` from the CSV cell.
"""

import gc
import time
import torch

# Candidates to try (edit if you want). Goes low → high so OOM stops early.
BATCH_CANDIDATES = [16, 24, 32, 48, 64]
N_TRIAL_PROMPTS = 64          # prompts used per successful batch-size trial
MAX_NEW_TOKENS_TRIAL = MAX_NEW_TOKENS  # keep same as full run

trial_prompts = df_base["query"].astype(str).head(N_TRIAL_PROMPTS).tolist()
if len(trial_prompts) < 1:
    raise RuntimeError("df_base has no prompts — run the CSV cell first.")


def _generate_batch(prompts_batch, max_new_tokens):
    formatted = []
    for p in prompts_batch:
        messages = [{"role": "user", "content": p}]
        try:
            text = tokenizer.apply_chat_template(
                messages, tokenize=False, add_generation_prompt=True
            )
        except Exception:
            text = f"User: {p}\nAssistant:"
        formatted.append(text)

    tokenizer.padding_side = "left"
    inputs = tokenizer(
        formatted, return_tensors="pt",
        padding=True, truncation=True, max_length=2048,
    ).to(model.device)

    with torch.no_grad():
        _ = model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            do_sample=DO_SAMPLE,
            temperature=TEMPERATURE,
            pad_token_id=tokenizer.pad_token_id,
        )


def _clear_cuda():
    gc.collect()
    if torch.cuda.is_available():
        torch.cuda.empty_cache()
        torch.cuda.synchronize()


results = []
print(f"Testing batch sizes on {len(trial_prompts)} prompts "
      f"(max_new_tokens={MAX_NEW_TOKENS_TRIAL}, greedy T={TEMPERATURE})")
print("-" * 64)

for bs in BATCH_CANDIDATES:
    if bs > len(trial_prompts):
        print(f"batch={bs:>3}  skipped (need ≥{bs} trial prompts)")
        continue

    _clear_cuda()
    # warmup 1 batch (not timed)
    try:
        _generate_batch(trial_prompts[:bs], MAX_NEW_TOKENS_TRIAL)
        _clear_cuda()
    except torch.cuda.OutOfMemoryError:
        print(f"batch={bs:>3}  OOM on warmup — stopping larger sizes")
        _clear_cuda()
        break
    except Exception as e:
        print(f"batch={bs:>3}  warmup error: {e}")
        _clear_cuda()
        break

    # timed pass: as many full batches as fit in trial_prompts
    n_batches = len(trial_prompts) // bs
    n_used = n_batches * bs
    try:
        if torch.cuda.is_available():
            torch.cuda.synchronize()
        t0 = time.perf_counter()
        for i in range(0, n_used, bs):
            _generate_batch(trial_prompts[i:i + bs], MAX_NEW_TOKENS_TRIAL)
        if torch.cuda.is_available():
            torch.cuda.synchronize()
        elapsed = time.perf_counter() - t0
        pps = n_used / elapsed if elapsed > 0 else 0.0
        mem_gb = (
            torch.cuda.max_memory_allocated() / (1024 ** 3)
            if torch.cuda.is_available() else float("nan")
        )
        results.append({"batch_size": bs, "prompts_per_sec": pps, "seconds": elapsed,
                        "n_prompts": n_used, "peak_mem_gb": mem_gb})
        print(f"batch={bs:>3}  {pps:6.2f} prompts/s  "
              f"({elapsed:.1f}s for {n_used} prompts)  peak_mem≈{mem_gb:.1f} GB")
        if torch.cuda.is_available():
            torch.cuda.reset_peak_memory_stats()
    except torch.cuda.OutOfMemoryError:
        print(f"batch={bs:>3}  OOM on timed run — stopping larger sizes")
        _clear_cuda()
        break
    except Exception as e:
        print(f"batch={bs:>3}  error: {e}")
        _clear_cuda()
        break

print("-" * 64)
if not results:
    raise RuntimeError("All batch sizes failed. Try LOAD_IN_4BIT=True or shorter prompts.")

best = max(results, key=lambda r: r["prompts_per_sec"])
BATCH_SIZE = int(best["batch_size"])

print(f"BEST batch size: {BATCH_SIZE}  "
      f"({best['prompts_per_sec']:.2f} prompts/s, peak_mem≈{best['peak_mem_gb']:.1f} GB)")

# Rough ETA for full dataset at this speed
n_full = len(df_base)
eta_sec = n_full / best["prompts_per_sec"] if best["prompts_per_sec"] > 0 else float("inf")
h, rem = divmod(int(eta_sec), 3600)
m, s = divmod(rem, 60)
print(f"ETA for {n_full:,} prompts @ this speed: {h}h {m}m {s}s")
print(f"BATCH_SIZE is now set to {BATCH_SIZE} for the full inference cell.")

"""## Cell 6 — Batched Inference (greedy decoding, T=0)

Uses `BATCH_SIZE` from the finder above (or from Config if you skipped that cell).
"""

import time


def generate_responses(
    model, tokenizer, prompts: List[str],
    batch_size: int = 8, max_new_tokens: int = 50,
    temperature: float = 0.0, do_sample: bool = False,
) -> List[str]:
    """Run batched inference over a list of prompts (greedy by default)."""
    responses: List[str] = []
    total = len(prompts)

    for start in range(0, total, batch_size):
        end = min(start + batch_size, total)
        batch_prompts = prompts[start:end]

        formatted = []
        for p in batch_prompts:
            messages = [{"role": "user", "content": p}]
            try:
                text = tokenizer.apply_chat_template(
                    messages, tokenize=False, add_generation_prompt=True
                )
            except Exception:
                text = f"User: {p}\nAssistant:"
            formatted.append(text)

        tokenizer.padding_side = "left"
        inputs = tokenizer(
            formatted, return_tensors="pt",
            padding=True, truncation=True, max_length=2048,
        ).to(model.device)

        with torch.no_grad():
            outputs = model.generate(
                **inputs,
                max_new_tokens=max_new_tokens,
                do_sample=do_sample,
                temperature=temperature,
                pad_token_id=tokenizer.pad_token_id,
            )

        for i, output_ids in enumerate(outputs):
            input_len = inputs["input_ids"][i].shape[0]
            generated_ids = output_ids[input_len:]
            text = tokenizer.decode(generated_ids, skip_special_tokens=True)
            responses.append(text.strip())

        if ((start // batch_size) + 1) % 10 == 0 or end == total:
            print(f"    [{end:>6,} / {total:>6,}] prompts processed")

    return responses


prompts = df_base["query"].tolist()
print(f"Generating responses (batch_size={BATCH_SIZE}, max_new_tokens={MAX_NEW_TOKENS}, greedy T={TEMPERATURE}) ...")
t0 = time.time()
responses = generate_responses(
    model, tokenizer, prompts,
    batch_size=BATCH_SIZE,
    max_new_tokens=MAX_NEW_TOKENS,
    temperature=TEMPERATURE,
    do_sample=DO_SAMPLE,
)
elapsed = time.time() - t0
print(f"Inference completed in {elapsed / 60:.1f} minutes.")

df = df_base.copy()
df["model_response"] = responses
os.makedirs(OUTPUT_DIR, exist_ok=True)
out_csv = os.path.join(OUTPUT_DIR, "baseline_llama.csv")
